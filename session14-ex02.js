//Thêm và sửa thuộc tính của đối tượng car
//Tạo một đối tượng car với các thuộc tính sau:

//brand: "Toyota"
//model: "Corolla"
//year: 2020
//Thêm một thuộc tính mới là color với giá trị "red"
//Cập nhật giá trị của thuộc tính year thành 2022
//In ra đối tượng sau khi cập nhật
 
let car = {
    brand: "Toyota",
    model: "Corolla",
    year: 2020
};
car.color = "red"; // Thêm thuộc tính color
car.year = 2022; // Cập nhật thuộc tính year
console.log(car); // In ra đối tượng car sau khi cập nhật
